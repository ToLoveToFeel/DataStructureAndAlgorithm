#pragma once

#include <iostream>
#include <cassert>

using namespace std;

// 我们的第三版Union-Find
// 在UF2的基础上合并集合时 考虑集合的大小
namespace UF3 {
	class UnionFind
	{
	private:
		// 我们的第三版Union-Find, 使用一个数组构建一棵指向父节点的树
		// parent[i]表示第一个元素所指向的父节点
		int* parent;
		int* sz;     // sz[i]表示以i为根的集合中元素个数
		int count;	// 数据个数

	public:
		UnionFind(int n);
		~UnionFind();

		// 查找过程, 查找元素p所对应的集合编号
		int find(int p);

		// 查看元素p和元素q是否所属一个集合
		// O(h)复杂度
		bool isConnected(int p, int q);

		// 合并元素p和元素q所属的集合
		// O(h) 复杂度
		void unionElements(int p, int q);

	};

	UnionFind::UnionFind(int  n) {
		count = n;

		parent = new int[n];
		sz = new int[n];
		// 初始化, 每一个id[i]指向自己, 没有合并的元素
		for (int i = 0; i < n; i++) {
			parent[i] = i;
			sz[i] = 1;
		}
			
	}

	UnionFind::~UnionFind() {
		delete[] parent;
		delete[] sz;
	}

	int UnionFind::find(int p) {
		assert(p >= 0 && p < count);

		// 不断去查询自己的父亲节点, 直到到达根节点
		// 根节点的特点: parent[p] == p
		while (p != parent[p])
			p = parent[p];
		return p;
	}

	bool UnionFind::isConnected(int p, int q) {
		return find(p) == find(q);
	}

	void UnionFind::unionElements(int p, int q) {
		int pRoot = find(p);
		int qRoot = find(q);

		if (pRoot == qRoot)
			return;

		// 根据两个元素所在树的元素个数不同判断合并方向
		// 将元素个数少的集合合并到元素个数多的集合上
		if (sz[pRoot] < sz[qRoot]) {
			parent[pRoot] = qRoot;
			sz[qRoot] += sz[pRoot];
		}
		else {
			parent[qRoot] = pRoot;
			sz[pRoot] += sz[qRoot];
		}
			
	}
}
