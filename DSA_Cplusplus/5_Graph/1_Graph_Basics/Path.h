#pragma once
#include <vector>
#include <stack>
#include <iostream>
#include <cassert>

using namespace std;

// 路径查询
template <typename Graph>
class Path {
private:
	Graph& G;
	int s;      // 起始点
	bool* visited;	// 记录该节点是否访问过
	int* from;		// 记录路径, from[i]表示查找的路径上i的上一个节点

	// 图的深度遍历，递归算法
	void dfs(int v) {
		visited[v] = true;
		typename Graph::adjIterator adj(G, v);
		for (int i = adj.begin(); !adj.end(); i = adj.next())
			if (!visited[i]) {
				from[i] = v;
				dfs(i);
			}
	}

public:
	// 构造函数
	Path(Graph& graph, int s) :G(graph) {
		visited = new bool[G.V()];
		from = new int[G.V()];
		for (int i = 0; i < G.V(); i++)
		{
			visited[i] = false;
			from[i] = -1;
		}
		this->s = s;

		// 寻路算法
		dfs(s);

	}

	// 析构函数
	~Path() {
		delete[] visited;
		delete[] from;
	}

	// 查询从s点到w点是否有路径
	int hasPath(int w) {
		assert(w >= 0 && w < G.V());
		return visited[w];
	}

	// 查询从s点到w点的路径, 存放在vec中
	void path(int w, vector<int>& vec) {
		assert(hasPath(w));

		stack<int> s;

		// 通过from数组逆向查找到从s到w的路径, 存放到栈中
		int p = w;
		while (-1 != p)
		{
			s.push(p);
			p = from[p];
		}
		
		// 从栈中依次取出元素, 获得顺序的从s到w的路径
		vec.clear();
		while (!s.empty())
		{
			vec.push_back(s.top());
			s.pop();
		}
	}

	// 打印出从s点到w点的路径
	void showPath(int w) {
		vector<int> vec;
		path(w, vec);
		for (int i = 0; i < vec.size(); i++) {
			cout << vec[i];
			if (i == vec.size() - 1)
				cout << endl;
			else
				cout << " -> ";
		}
	}

};